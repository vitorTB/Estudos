<h2>Encapsulamento</h2>
<p>Encapsulamento é a ideia de que os dados de um objeto devem ser
    acessados apenas através de métodos do próprio objeto. Isso significa que os dados de
    um objeto não podem ser acessados diretamente, apenas através de métodos que
    permitem a manipulação desses dados.</p>
<hr>
<h2>Herança</h2>
<p>Herança é a capacidade de um objeto criar uma nova classe a partir de uma classe
    existente. Isso permite que o novo objeto herde as propriedades e métodos
    da classe existente e adicione novas propriedades e métodos ou sobres escreva os existentes.</p>
<h2>Polimorfismo</h2>
<p>Polimorfismo é a capacidade de um objeto se comportar de maneira diferente
    dependendo do contexto em que ele está sendo usado. Isso pode ser alcançado
    através da sobrecarga de métodos ou da sobrescrita de métodos.</p>
<p>
    Existem duas formas de polimorfimos existe o estático e dinâmico
</p>
<h2>abstração</h2>
<p>
    Abstração é a capacidade de um objeto se comportar de maneira independente
    do contexto em que ele está sendo usado. Isso pode ser alcançado através da
    criação de métodos que encapsulem a lógica de negócios do objeto
</p>